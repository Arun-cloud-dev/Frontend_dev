// Reuse the function
function calculateRectangleArea(width, height) {
    return width * height;
}

// Calculate the area of the first rectangle
const rectangleWidth = 5;
const rectangleHeight = 10;
const area1 = calculateRectangleArea(rectangleWidth, rectangleHeight);
console.log(`Area of rectangle:${area1}`)

// Calculate the area of the second rectangle

const rectangle2Width = 8;
const rectangle2Height = 15;
const area2 = calculateRectangleArea(rectangle2Width, rectangle2Height);
console.log(`Area of rectangle:${area2}`);





